function [ V ] = p1( R )
% 做二值化
% P1 将矩阵R转换为矩阵V，达到下面的效果：
% R中的任意一个元素若离1更近，则V中相应的元素为1；若离-1更近，则V中相应的元素为-1

% a=[0.5 1 3;2 6 0.1;8 0.3 4]
% b=find(a - 1<0)
% a(b)=1;
% a
%  R=[0.5 1 3;2 6 0.1;8 0.3 4]-1;
V = zeros(size(R));
t = find(abs(R-1) <= abs(R+1));
V(t) = 1;
t = find(abs(R-1) > abs(R+1));
V(t) = -1;


end

